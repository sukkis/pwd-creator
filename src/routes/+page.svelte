<script lang="ts">
  import { invoke } from "@tauri-apps/api/core";
  let length = $state(15);
  let generatedPassword = $state("");

  async function generate_password(event: Event) {
    event.preventDefault();
    generatedPassword = await invoke("password", { length });
  }


</script>

<main class="container">
  <h1>Generate Password</h1>

  <form class="row" onsubmit={generate_password}>
    <div class="column">
      <input type="range" bind:value={length} min="0" max="32" step="1" />
      <span>{length}</span>
    </div>

    <div class="column">
      <button type="submit">Generate</button>
    </div>
  </form>

  <p>{generatedPassword}</p>
</main>

